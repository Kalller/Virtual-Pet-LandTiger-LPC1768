void PIC_DRAWER(int posX, int posY, int animation ){
	
	int x, y, i, color;
	int w=WIDTH;
	int h=HEIGHT;
	
	uint16_t* Pic_p;  // puntatore all'immagine in formato rgb (sono dichiarate in alto) da stampare
	//animazione base
	if(animation==0)
		Pic_p = (uint16_t*)&(BREATH_0_pixel_data);
	if(animation==1)
		Pic_p = (uint16_t*)&(BREATH_1_pixel_data);
	//animazione cuddle
	if(animation==2)
		Pic_p = (uint16_t*)&(CUDDLE_pixel_data);
	if(animation==3)
		Pic_p = (uint16_t*)&(CUDDLE_1_pixel_data);
	if(animation==4)
		Pic_p = (uint16_t*)&(CUDDLE_2_pixel_data);
	//volume
	if(animation==-1){
		Pic_p = (uint16_t*)&(SPEAKER_pixel_data);
	}
	if(animation==-2){
		Pic_p = (uint16_t*)&(MUTE_pixel_data);
	}
	if(animation==-3){
		Pic_p = (uint16_t*)&(L1_pixel_data);
	}
	if(animation==-4){
		Pic_p = (uint16_t*)&(L2_pixel_data);
	}
	if(animation==-5){
		Pic_p = (uint16_t*)&(L3_pixel_data);
	}
	//bianco
	if(animation==10)
		Pic_p = (uint16_t*)&(WHITE_pixel_data);
	//stats
	if(animation==13)
		Pic_p = (uint16_t*)&(H3_pixel_data);
	
	if(animation==12)
		Pic_p = (uint16_t*)&(H2_pixel_data);
	
	if(animation==11)
		Pic_p = (uint16_t*)&(H1_pixel_data);
	
	if(animation==23)
		Pic_p = (uint16_t*)&(S3_pixel_data);
	
	if(animation==22)
		Pic_p = (uint16_t*)&(S2_pixel_data);
	
	if(animation==21)
		Pic_p = (uint16_t*)&(S1_pixel_data);
	//fine stats
	if(animation==7)  //esplosione 1
		Pic_p = (uint16_t*)&(D1_pixel_data);
	if(animation==8)  //esplosione 2
		Pic_p = (uint16_t*)&(D2_pixel_data);
	if(animation==9){ //esplosione 3
		Pic_p = (uint16_t*)&(D3_pixel_data);
		w=76;
		h=60;
	}
	if(animation>10){
		w=54;
		h=18;
	}
  if(animation<0)
		w=h=10;
	
	x=y=0;
	
	for(i=0;i<w*h;i++){
		uint16_t Pic=*Pic_p++;
		uint8_t r=(Pic & 0xf800) >> 8;
		uint8_t g=(Pic & 0x07e0) >> 3;
		uint8_t b=(Pic & 0x001f) << 3;
		
		if(y==w){
			x++;
			y=0;
	}
		color=RGB565CONVERT(r,g,b);
		LCD_SetPoint(posX+y,posY+x,color);
		y++;
}


}

